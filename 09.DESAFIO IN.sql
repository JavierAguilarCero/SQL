/*
ESTE CÓDIGO SQL RECUPERA TODOS LOS PAGOS REALIZADOS POR UN GRUPO ESPECÍFICO DE CLIENTES (ID: 12, 25, 67, 93, 124, 234), CUYOS MONTOS SEAN EXACTAMENTE 4.99, 7.99 O 9.99
DÓLARES, Y QUE HAYAN OCURRIDO DURANTE EL MES DE ENERO DE 2020.
*/
SELECT 
	*
FROM 
	payment
WHERE 
	(customer_id IN (12,25,67,93,124,234))
	AND (amount IN (4.99,7.99,9.99))
	AND (payment_date BETWEEN '2020-01-01' AND '2020-01-31 23:59')